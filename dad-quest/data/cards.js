// `effect` is a placeholder string. Phase 2 wires executable handlers.
// Effect grammar (chosen for consistency, simple to parse):
//   "deal_damage:N"                           deal N to target
//   "deal_damage_all:N"                       deal N to all enemies
//   "deal_damage_random:N:hits"               deal N to random enemy, repeated hits times
//   "gain_block:N"                            gain N block
//   "apply:status:N"                          apply N stacks of status to target
//   "apply_all:status:N"                      apply N stacks of status to all enemies
//   "apply_random:status:N"                   distribute N stacks of status across random enemies
//   "draw:N"                                  draw N cards
//   "gain:resource:N"                         gain N of a resource (yard_work, caffeine, strength, gold, energy)
//   "heal:N"                                  heal N HP
//   "lose_hp:N"                               lose N HP (self damage)
//   "compound:effect_a,effect_b,..."          chain multiple effects in order
//   "conditional:cond:effect"                 run effect only if cond is true
//                                             cond examples: caffeine_gte:3, citation_gte:5, gained_caffeine_this_turn,
//                                             yard_work_gte:3, applied_citation_this_turn
//   "scaling:expr"                            damage equals an expression
//                                             expr examples: yard_work*3, cards_played_this_turn*3, total_citations*2
//   "spend_yard_work_bonus:N"                 spend all yard work, deal +N damage per stack spent
//   "add_to_hand:filter:N:cost_zero_this_turn"  add N random cards matching filter to hand at cost 0 for the turn
//   "add_to_discard:card_id"                  shuffle a specific card into discard pile
//   "upgrade_hand_for_combat"                 Apotheosis-only: double all numbers on hand cards for this combat
//   "heal_damage_taken_this_turn"             Last Stand-only: heal HP equal to damage taken this turn
//   "next_turn:effect"                        queue effect to apply at start of next player turn
//   "first_damage_prevent"                    next instance of damage taken this turn is prevented

